package pkg007bucles;

public class BucleFor {

    public static void main(String[] args) {

        
        for (int i = 100; i < 1000; i++) {
            System.out.println(i);
        }
       
        
        for (int i = 100; i > 10; i--) {
            System.out.println(i);
        }
    }

}
