package pkg007bucles;

public class BucleWhile {
    
    public static void main(String[] args) {
        
        int contador = 0;
        
        System.out.println("Abans del bucle");
        while (contador < 10){
            
            System.out.println("Hola alumnes!!!!");
            contador++;
            
        }
        
        System.out.println("Fora del bucle");
        
    }
    
}
